function emptyLine(count = 1) {
  const lines = [];
  for (let i = 0; i < count; i++) {
    lines.push(new Paragraph({ spacing: { after: 120 }, children: [] }));
  }
  return lines;
}

function sectionTitle(text, number) {
  return new Paragraph({
    spacing: { before: 400, after: 200 },
    border: {
      bottom: { style: BorderStyle.SINGLE, size: 6, color: COLORS.HOT_RED },
    },
    children: [
      new TextRun({
        text: `${number}. `,
        bold: true,
        size: 32,
        color: COLORS.HOT_RED,
        font: "Segoe UI",
      }),
      new TextRun({
        text: text.toUpperCase(),
        bold: true,
        size: 32,
        color: COLORS.DARK,
        font: "Segoe UI",
      }),
    ],
  });
}


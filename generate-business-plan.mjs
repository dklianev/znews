// Таблица с чередоване на редове
function styledTable(headers, rows) {
  const headerCells = headers.map(
    (h) =>
      new TableCell({
        shading: { type: ShadingType.CLEAR, fill: COLORS.DARK },
        margins: {
          top: 80, bottom: 80, left: 120, right: 120,
        },
        children: [
          new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [
              new TextRun({
                text: h,
                bold: true,
                size: 20,
                color: COLORS.WHITE,
                font: "Segoe UI",
              }),
            ],
          }),
        ],
      })
  );

  const dataRows = rows.map(
    (row, idx) =>
      new TableRow({
        children: row.map(
          (cell) =>
            new TableCell({
              shading: {
                type: ShadingType.CLEAR,
                fill: idx % 2 === 0 ? COLORS.LIGHT_GRAY : COLORS.WHITE,
              },
              margins: {
                top: 60, bottom: 60, left: 120, right: 120,
              },
              children: [
                new Paragraph({
                  children: [
                    new TextRun({
                      text: cell,
                      size: 20,
                      color: COLORS.DARK,
                      font: "Segoe UI",
                    }),
                  ],
                }),
              ],
            })
        ),
      })
  );

  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows: [new TableRow({ children: headerCells }), ...dataRows],
  });
}

